;PROGRAM TO ADD TWO NUMBERS USING VARIABLE
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
STT1 DB ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,1                         ;TAKING FIRST INPUT
INT 21H
MOV STT1,AL                      ;MOVING VALUE IN STT1 VARIABLE
SUB STT1,48

MOV AH,1                         ;TAKING SECOND INPUT
INT 21H
SUB AL,48

ADD STT1,AL                      ;ADDING BOTH VALUES
ADD STT1,48                      
MOV DL,STT1
MOV AH,2                         ;PRINTING SUM VALUE
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
