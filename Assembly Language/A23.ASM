;PROGRAM FOR INPUT TWO NUMBERS AND ADD THEM
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV AH,1           ;TAKING INPUT
INT 21H
MOV BL,AL          ;MOVING FROM AL TO BL REGISTER
SUB BL,48          ;SUBTRACT 48 BECAUSE IT IS IN ASCII
MOV AH,1           ;TAKING SECOND INPUT
INT 21H
MOV DL,AL          ;MOVING FROM AL TO DL REGISTER
SUB DL,48          ;SUBTRACT 48 BECAUSE IT IS IN ASCII
ADD DL,BL          ;ADDING BOTH NUMBERS
ADD DL,48          ;ADDING 48 BECAUSE PRINTING IS IN ASCII
MOV AH,2           ;PRINTING
INT 21H
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
