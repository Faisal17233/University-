;PROGRAM FOR OR,AND,XOR
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV BL,101B
MOV CL,110B
OR BL,CL                  ;OR OPERATION
ADD BL,48
MOV DL,BL
MOV AH,2
INT 21H

MOV DX,10                 ;NEXT LINE
MOV AH,2
INT 21H

MOV DX,13
MOV AH,2
INT 21H

MOV BL,111B               ;AND OPERATION
AND BL,CL
ADD BL,48
MOV DL,BL
MOV AH,2
INT 21H

MOV DX,10                 ;NEXT LINE
MOV AH,2
INT 21H

MOV DX,13
MOV AH,2
INT 21H

MOV BL,100B               ;XOR OPERATION
XOR BL,CL
ADD BL,48
MOV DL,BL
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN














