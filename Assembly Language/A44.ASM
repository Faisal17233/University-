;PROGRAM OF DIVISION IN LOOP
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
F1 DW ?
F2 DB ?
F3 DB ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV CX,4
MOV AX,2
MOV BL,4

L1:                       ;LOOP
MOV F1,AX                 ;SAVING VALUE OF DIVIDEND
DIV BL                    ;DIVIDE
MOV F2,AL
MOV F3,AH
MOV DL,F2                 ;PRINTING REMAINDER
ADD DL,48
MOV AH,2                  
INT 21H
MOV DL,F3                 ;PRINTING QUOTIENT
ADD DL,48           
MOV AH,2
INT 21H
MOV DL,'-'
MOV AH,2
INT 21H
MOV AX,F1                 ;RESTORING SAVED VALUE OF DIVIDEND
ADD AX,3
LOOP L1

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
