;PROGRAM TO SUBTRACT TWO NUMBERS
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV BL,3                  ;STORING 3 IN BL REGISTER
MOV DL,6                  ;STORING 6 IN BL REGISTER
SUB DL,BL                 ;SUBTRACTING 6 WITH 3
ADD DL,48                 ;ADDING 48 BECAUSE OF ASCII
MOV AH,2                  ;PRINTING
INT 21H
MOV AH,4CH                ;EXITING REGISTER
INT 21H
MAIN ENDP
END MAIN
